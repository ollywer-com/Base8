            font: 15px Consolas, Monaco, monospace;
        }

        .card {
            max-width: 720px;
            background: #fff;
            border: 1px solid #ddd;
            padding: 24px;
        }

        h1 {
            margin: 0 0 24px;
            font-size: 24px;
        }

        table {
            width: 100%;
            border-collapse: collapse;
        }

        td {
            padding: 8px 0;
        }

        td:first-child {
            width: 180px;
            font-weight: bold;
        }

        .success {
            margin-top: 24px;
            color: #198754;
            font-weight: bold;
        }

    </style>

</head>

<body>

<div class="card">

    <h1>Base8 Framework Builder</h1>

    <table>

        <tr>
            <td>Source</td>
            <td>src</td>
        </tr>

        <tr>
            <td>Output</td>
            <td>Base8.php</td>
        </tr>

        <tr>
            <td>Files</td>
            <td><?= $count ?></td>
        </tr>

        <tr>
            <td>Output size</td>
            <td><?= $size ?> KB</td>
        </tr>

        <tr>
            <td>Build time</td>
            <td><?= $buildTime ?></td>
        </tr>

        <tr>
            <td>Duration</td>
            <td><?= $duration ?> ms</td>
        </tr>

    </table>

    <p class="success">✔ Build completed successfully.</p>

</div>

</body>

</html>