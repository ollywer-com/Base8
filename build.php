<?php

declare(strict_types=1);

$start = microtime(true);

$source = __DIR__ . '/src';
$output = __DIR__ . '/framework/Base8.php';

if (!is_dir($source)) {
    exit('Missing source directory.');
}

$files = glob($source . '/*.php');

if ($files === false || $files === []) {
    exit('No PHP files found.');
}

sort($files, SORT_STRING);

$header = <<<'PHP'
<?php

declare(strict_types=1);

PHP;

$buildTime = date('Y-m-d H:i:s');

$build = <<<PHP
<?php

/*
|--------------------------------------------------------------------------
| Base8 Framework
|--------------------------------------------------------------------------
|
| This file is automatically generated.
|
| Build time: {$buildTime}
|
| Do not edit this file manually.
|
*/

declare(strict_types=1);

PHP;

foreach ($files as $file) {

    $code = file_get_contents($file);

    if ($code === false) {
        exit("Cannot read {$file}.");
    }

    if (str_starts_with($code, "\xEF\xBB\xBF")) {
        exit("UTF-8 BOM is not allowed: {$file}");
    }

    if (!str_starts_with($code, $header)) {

        exit(
            "Invalid source file: {$file}\n\n" .
            "Every source file must begin with:\n\n" .
            "<?php\n\n" .
            "declare(strict_types=1);\n"
        );
    }

    $code = substr($code, strlen($header));

    $build .= rtrim($code) . PHP_EOL . PHP_EOL;
}

$build = rtrim($build) . PHP_EOL;

if (file_put_contents($output, $build) === false) {
    exit("Cannot write {$output}");
}

$duration = number_format(
    (microtime(true) - $start) * 1000,
    2
);

$size = number_format(
    filesize($output) / 1024,
    2
);

$count = count($files);

if (PHP_SAPI === 'cli') {

    echo PHP_EOL;
    echo "======================================================" . PHP_EOL;
    echo "                 Base8 Framework Builder" . PHP_EOL;
    echo "======================================================" . PHP_EOL;
    echo PHP_EOL;
    echo " Source       : src" . PHP_EOL;
    echo " Output       : Base8.php" . PHP_EOL;
    echo " Files        : {$count}" . PHP_EOL;
    echo " Output size  : {$size} KB" . PHP_EOL;
    echo " Build time   : {$buildTime}" . PHP_EOL;
    echo " Duration     : {$duration} ms" . PHP_EOL;
    echo PHP_EOL;
    echo " Status       : SUCCESS" . PHP_EOL;
    echo PHP_EOL;
    echo "======================================================" . PHP_EOL;

    exit();
}

header('Content-Type: text/html; charset=utf-8');

?>
<!DOCTYPE html>
<html lang="en">

<head>

    <meta charset="utf-8">

    <title>Base8 Builder</title>

    <style>

        body {
            margin: 40px;
            background: #f5f5f5;
